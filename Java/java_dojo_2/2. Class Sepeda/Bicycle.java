//Tentukan class 'Bicycle'
class Bicycle {
  //Deklarasikan field instance 'name' bertipe private dan return String
  private String name;
  //Tentukan constructor untuk class Bicycle
  Bicycle(String name) {
    this.name = name;
  } 
  //Tentukan method getter
  public String getName() {
    return this.name;
  }
}